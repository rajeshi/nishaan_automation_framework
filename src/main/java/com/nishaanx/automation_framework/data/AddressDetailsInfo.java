package com.nishaanx.automation_framework.data;

public class AddressDetailsInfo {

    String addressLine;
    String townCity;
    String postCode;
    String country;
    boolean isCorrespondenceDifferent;
    String correspondenceAddressLine;
    String correspondenceTownCity;
    String correspondencePostCode;
    String correspondenceCountry;

    public String getAddressLine() {
        return addressLine;
    }

    public void setAddressLine(String addressLine) {
        this.addressLine = addressLine;
    }

    public String getTownCity() {
        return townCity;
    }

    public void setTownCity(String townCity) {
        this.townCity = townCity;
    }

    public String getPostCode() {
        return postCode;
    }

    public void setPostCode(String postCode) {
        this.postCode = postCode;
    }

    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public boolean isIsCorrespondenceDifferent() {
        return isCorrespondenceDifferent;
    }

    public void setIsCorrespondenceDifferent(boolean isCorrespondenceDifferent) {
        this.isCorrespondenceDifferent = isCorrespondenceDifferent;
    }

    public String getCorrespondenceAddressLine() {
        return correspondenceAddressLine;
    }

    public void setCorrespondenceAddressLine(String correspondenceAddressLine) {
        this.correspondenceAddressLine = correspondenceAddressLine;
    }

    public String getCorrespondenceTownCity() {
        return correspondenceTownCity;
    }

    public void setCorrespondenceTownCity(String correspondenceTownCity) {
        this.correspondenceTownCity = correspondenceTownCity;
    }

    public String getCorrespondencePostCode() {
        return correspondencePostCode;
    }

    public void setCorrespondencePostCode(String correspondencePostCode) {
        this.correspondencePostCode = correspondencePostCode;
    }

    public String getCorrespondenceCountry() {
        return correspondenceCountry;
    }

    public void setCorrespondenceCountry(String correspondenceCountry) {
        this.correspondenceCountry = correspondenceCountry;
    }

}
