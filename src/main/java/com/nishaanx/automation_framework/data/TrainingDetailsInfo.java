package com.nishaanx.automation_framework.data;

public class TrainingDetailsInfo {

    String expectedToFinish;
    String conductedInEnglish;
    String nationalTrainingNumber;
    String whichDeanery;
    String medicalSchoolUkOrOtherCountry;
    String trainingPathWay;

    public String getExpectedToFinish() {
        return expectedToFinish;
    }

    public void setExpectedToFinish(String expectedToFinish) {
        this.expectedToFinish = expectedToFinish;
    }

    public String getConductedInEnglish() {
        return conductedInEnglish;
    }

    public void setConductedInEnglish(String conductedInEnglish) {
        this.conductedInEnglish = conductedInEnglish;
    }

    public String getNationalTrainingNumber() {
        return nationalTrainingNumber;
    }

    public void setNationalTrainingNumber(String nationalTrainingNumber) {
        this.nationalTrainingNumber = nationalTrainingNumber;
    }

    public String getWhichDeanery() {
        return whichDeanery;
    }

    public void setWhichDeanery(String whichDeanery) {
        this.whichDeanery = whichDeanery;
    }

    public String getMedicalSchoolUkOrOtherCountry() {
        return medicalSchoolUkOrOtherCountry;
    }

    public void setMedicalSchoolUkOrOtherCountry(String medicalSchoolUkOrOtherCountry) {
        this.medicalSchoolUkOrOtherCountry = medicalSchoolUkOrOtherCountry;
    }

    public String getTrainingPathWay() {
        return trainingPathWay;
    }

    public void setTrainingPathWay(String trainingPathWay) {
        this.trainingPathWay = trainingPathWay;
    }

}
